# Numeros

entero = 10
flotante = 10.5
negativo = -55
par = 6
impar = 5

# Metodos sobre numneros

# Para convertir entre enteros y flotantes
entero_a_flotante = entero.to_f
flotante_a_entero = flotante.to_i
puts entero_a_flotante
puts flotante_a_entero

# Para saber la magnitud
positivo = negativo.abs
puts positivo

# Para saber si es par o impar
es_par_6 = par.even?
es_par_5 = impar.even?
puts es_par_5
puts es_par_6

